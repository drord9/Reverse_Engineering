void keygen(string& key)
{
	for (size_t i = 0; i < key.size(); i++)
	{
		char index = key[i] - ' ';
		char newVal = arr[index];
		key[i] = newVal;
	}
}


void keygen_rev(string& key)
{
	for (size_t i = 0; i < key.size(); i++)
	{
		char val = key[i];
		char index = ( std::find(arr, arr + sizeof(arr), val) - arr );
		char newVal = index + ' ';
		key[i] = newVal;
	}
}


